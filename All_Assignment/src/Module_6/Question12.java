package Module_6;

public class Question12 {

	
	public static void main(String[] args) {
		
		
		/*
	     * 
	     * 12. sum value of an array
	     * 
	     */
	        
	         int [] r = new int [] {3, 4, 5};  
	         int sum1 = 0;  
	         
	         for (int i = 0; i < r.length; i++) {  
	            sum1 = sum1 + r[i];  
	         }  
	         System.out.println("Sum of all the elements of an array: " + sum1);  
	     
	         
	}
}
